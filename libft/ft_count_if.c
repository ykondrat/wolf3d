int		ft_count_if(char *str, int (*f)(char*))
{
	int i;
	int j;

	i = 0;
	j = 0;
	if (!str || !f)
		return (0);
	while (str[i])
	{
		if (f(&str[i]) == 1)
			j++;
		i++;
	}
	return (j);
}
